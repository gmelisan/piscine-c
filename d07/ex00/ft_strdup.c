#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *result;

	result = (char*)malloc(sizeof(*src));
	if (result == 0)
		return (0);
	ft_strcpy(result, src);
	return (result);
}
