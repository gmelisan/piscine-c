char	*ft_strrev(char *str)
{
	int n;
	int m;
	int temp;

	n = -1;
	m = 0;
	while (str[m] != '\0')
	{
		m++;
	}
	while (++n < --m)
	{
		temp = str[n];
		str[n] = str[m];
		str[m] = temp;
	}
	return (str);
}
